module.exports = function(routes, module)
{
	/**
	 * Send a custom notification to the dashboards
	 */
	routes.get('/notification', function( req, res )
	{
		module.notification({
			message: req.params.message
		});

		res.send({
			status: 200,
			message: 'OK'
		});
	});
}